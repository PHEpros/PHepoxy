// Helper function to calculate price
export function calculatePrice(basePrice, size, material) {
  const sizeInfo = sizeOptions.find(s => s.id === size);
  let materialInfo = null;
  
  // Find material across all material categories
  for (const category in materials) {
    const found = materials[category].find(m => m.id === material);
    if (found) {
      materialInfo = found;
      break;
    }
  }
  
  if (!sizeInfo || !materialInfo) return basePrice;
  
  return (basePrice * sizeInfo.multiplier * materialInfo.priceMultiplier).toFixed(2);
}

// Get featured products
export function getFeaturedProducts() {
  return products.filter(p => p.featured);
}

// Get product by ID
export function getProductById(id) {
  return products.find(p => p.id === id);
}

// Get products by category
export function getProductsByCategory(categoryId) {
  if (!categoryId) return products;
  return products.filter(p => p.category === categoryId);
}
